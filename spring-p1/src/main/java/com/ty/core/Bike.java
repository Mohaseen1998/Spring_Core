package com.ty.core;

public class Bike {
	private String name;
	private double cost;
	private spec sp;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public double getCost() {
		return cost;
	}

	public void setCost(double cost) {
		this.cost = cost;
	}

	public spec getSp() {
		return sp;
	}

	public void setSp(spec sp) {
		this.sp = sp;
	}

}
